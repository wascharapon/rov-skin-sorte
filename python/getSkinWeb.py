            url = f"https://sortskin.com/_next/image?url=%2Fheros01%2F{base_name}%2F{skin_number_str}.png&w=3840&q=20"
            filename = f"{base_name.lower()}_{skin_number_str}.png"

            # ‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°‡∏î‡∏≤‡∏ß‡∏ô‡πå‡πÇ‡∏´‡∏•‡∏î
            result = download_image_from_url(url, filename)

